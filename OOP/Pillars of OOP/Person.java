public class Person {
    // Private fields
    private String gender;
    private String name;
    private int age;

    // Public methods to access and modify th private fields

    // Accessors/Getters
    public String getGender() {
        return gender;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }    


    // Mutators/Setters
    public void setGender(String gender) {
        this.gender = gender;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge (int age) {
        this.age = age;
    }

}
