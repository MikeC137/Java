public class comparisonoperators {
    public static void main(String[] args) {
        // Comparison operators are used to compare primitive values
        int x = 1;
        int y = 1;
        //Boolean expressions - true or false
        System.out.println(x == y); 
        System.out.println(x != y);
        System.out.println(x > y);
        System.out.println(x >= y);
        System.out.println(x < y);
        System.out.println(x <= y);
    }
}