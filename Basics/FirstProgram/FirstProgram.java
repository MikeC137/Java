package FirstProgram;

// Functions don't exist on their own. They should always belong in a class,
// a container for one or more related functions (think of supermarket section)
// Packages --> Classes --> Methods

public class FirstProgram {

    // Method: A function that is part of a class
    public static void main(String[] args) { 
        // Access Modifier: a keyword used to define the scope/level of accesibility
        // of classes, methods, and variables
        System.out.println("Hello World");
    }

}

